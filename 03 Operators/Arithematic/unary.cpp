#include <iostream>
using namespace std;

int main()
{
    int x = 5;

    // Unary Plus operator (+): Indicates positive values
    int y = +x; // Unary plus doesn't change the value
    cout << "Value of y after unary plus: " << y << endl;

    // Unary Minus operator (-): Negates the value of the operand
    int z = -x; // Unary minus negates the value
    cout << "Value of z after unary minus: " << z << endl;

    return 0;
}
