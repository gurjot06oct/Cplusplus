#include <iostream>
using namespace std;

// Standard I/O with `cin` and `cout`
int main()
{
    int a, b;
    cin >> a >> b;         // Input two integers
    cout << a + b << endl; // Output their sum
    return 0;
}
// **Advantages:**
// - Easy to use.
// - Well-integrated with C++ standard libraries.

// **Disadvantages:**
// - Slower compared to other methods, especially with large input/output.