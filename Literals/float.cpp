#include <iostream>

int main()
{
    // Float literals
    float temperature = 25.5f; // Using 'f' suffix to indicate float
    float velocity = 3.0e8f;   // Scientific notation with 'f' suffix

    // Outputting float literals
    std::cout << "Temperature: " << temperature << std::endl;
    std::cout << "Velocity: " << velocity << std::endl;

    return 0;
}
